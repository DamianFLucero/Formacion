let numero = 3;
let mensaje;

switch (numero){
    case 1:
        mensaje = 'Número UNO';
        break;
    case 2:
        mensaje = 'Número DOS';
        break;
    case 3:
        mensaje = 'Número TRES';
        break;
    case 4:
        mensaje = 'Número CUATRO'
        break;
    default:
        mensaje = 'No encontrado'
}

console.log(mensaje);